<?php 
namespace App;

class Maths {
	
   public function add($num1,$num2) {
      
      return $num1 + $num2;

   }

   public function sub($num1,$num2) {
     
      return $num1 - $num2;     

   }

   public function muliply($num1,$num2) {     
     
      return $num1 * $num2;

   }  
 
   public function devide($num1,$num2) {     
     
      return $num1 / $num2;     

   }     
   
}



?>